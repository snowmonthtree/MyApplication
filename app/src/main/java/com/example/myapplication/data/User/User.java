package com.example.myapplication.data.User;

public class User {


    private String userId;

    private String name;

    private String permissionId="-1";

    private String password;

    private String email;

    private String avatarWebUrl;

    private int likesNum;

    private int followersNum;

    // Getters and Setters
    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPermissionId() {
        return permissionId;
    }

    public void setPermissionId(String permissionId) {
        this.permissionId = permissionId;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getAvatarWebUrl() {
        return avatarWebUrl;
    }

    public void setAvatarWebUrl(String avatarWebUrl) {
        this.avatarWebUrl = avatarWebUrl;
    }

    public int getLikesNum() {
        return likesNum;
    }

    public void setLikesNum(int likesNum) {
        this.likesNum = likesNum;
    }

    public int getFollowersNum() {
        return followersNum;
    }

    public void setFollowersNum(int followersNum) {
        this.followersNum = followersNum;
    }
}
